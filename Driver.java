package hackathon2019;

public class Driver
{
		    private String name;
		    private String date_of_birth;
		    
		    public Driver(String n)
		    {
		    	name = n;
		    }
		    public String get_name()
		    {
		    	return name;
		    }
		    
		    public String get_date_of_birth()
		    {
		    	return date_of_birth;
		    }
		    
		    public void set_name(String n)
		    {
		    	name = n;
		    }
		    
		    public void set_date_of_birth(String db)
		    {
		    	date_of_birth = db;
		    }
};


