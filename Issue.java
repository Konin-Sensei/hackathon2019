package hackathon2019;

public class Issue{
	   
	private int timestamp;
	private String issue_name;
	private boolean issue_present;
	
	
	public int get_timestamp(){
		return timestamp;
	}
	
	public String get_issue()
	{
		return issue_name;
	}
	
	public boolean get_issue_present()
	{
		return issue_present;
	}
	};
